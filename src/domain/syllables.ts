/**
 * Выбрать случайные n слогов без повторений.
 * target включается в результат, остальные — случайные из списка.
 */
export function pickRandomSyllables(
  target: Syllable,
  count: number
): Syllable[] {
  const others = SYLLABLES.filter((s) => s !== target)
  if (others.length < count - 1) {
    throw new Error(`Not enough syllables for count ${count}`)
  }
  const shuffled = [...others].sort(() => Math.random() - 0.5)
  const rest = shuffled.slice(0, count - 1)
  const result = [target, ...rest]
  return result.sort(() => Math.random() - 0.5)
}
